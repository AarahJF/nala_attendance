class Student {
  String firstName;
  String lastName;
  String mathLocation;
  String readingLocation;
  String studentId;
  String TCAID;
  bool isReading;
  bool isDoingMath;
  bool isActive;

  Student({
    required this.firstName,
    required this.lastName,
    required this.mathLocation,
    required this.readingLocation,
    required this.studentId,
    required this.TCAID,
    required this.isReading,
    required this.isDoingMath,
    required this.isActive,
  });
}
